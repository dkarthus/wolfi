#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list *tmp1;
	t_list *tmp;

	if (!lst || !*lst)
		return ;
	tmp1 = *lst;
	while (tmp1)
	{
		tmp = tmp1;
		del(tmp1->content);
		tmp1 = tmp1->next;
		free(tmp);
	}
	*lst = NULL;
}
