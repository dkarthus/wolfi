#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char *ptr;

	ptr = (unsigned char *)dst;
	if (n == 0 || dst == src)
		return (dst);
	while (n != 0)
	{
		*ptr++ = *(unsigned char *)src++;
		n--;
	}
	return (dst);
}
